# Eksplorasi mengenai type data pada Python

# String
a = 'Hallo'
print(type(a))
print(a)

# Integer & Float
b = 3
c = 1.5
hasil = b/c
print(type(b))
print(type(c))
print(hasil)

# Boolean
d = True
print(type(d))

# Tipe data yang datanya berkelompok
# list
dataHewan = ['Singa', 'Kuda', 'Jerapah']
print(dataHewan)
print(type(dataHewan))

# Tuple
dataShinobi = ('Naruto', 'Sasuke', 'Sakura')
print(dataShinobi)
print(type(dataShinobi))

# Dict

dataDiri = {'Nama' : 'Rizal', 'Alamat' : 'Ponorogo', 'Gender' : 'Laki-Laki'}
print(dataDiri)
print(type(dataDiri))
# Tampilkan satu index
print(dataDiri['Nama'])
